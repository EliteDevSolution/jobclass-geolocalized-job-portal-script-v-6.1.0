namespace App\Http\Middleware;

use Closure;

class SetAppLocale
{
	/**
	 * Handle an incoming request.
	 *
	 * @param \Illuminate\Http\Request $request
	 * @param \Closure $next
	 * @return mixed
	 */
	public function handle($request, Closure $next)
	{
		if (session()->has('langCode')) {
			if (isAvailableLang(session()->get('langCode'))) {
				config()->set('app.locale', session()->get('langCode'));
			}
		}
		
		return $next($request);
	}
}
